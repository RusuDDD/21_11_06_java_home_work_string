package de.telran;

public class LoanUser {

    String name;
    String surname;
    int age;
    int anualSalary;

    public LoanUser(String name, String surname, int age, int anualSalary) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.anualSalary = anualSalary;
    }

}








